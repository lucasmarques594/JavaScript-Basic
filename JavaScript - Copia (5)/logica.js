/*  Oque é um programa/Software?

-> é um conjunto de declarões é um programa(statements);
-> Statements são conjuntos de expressões (expressions);
-> Expression é todo fragmento do código que produz um valor;
   
      sofware tem um 'objetivo';
      statements 'guiam' o software para o seu objetivo;
      Expressions são valores que os statements esperam para guiar o software;


-> Nós já vimos statements e expressions de forma simples;
-> statements: console.log(1>3);
-> expression: console.log('pão');
-> porém ainda não é o suficiente para criar um software;
*/